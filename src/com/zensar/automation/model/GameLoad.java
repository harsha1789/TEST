package com.zensar.automation.model;

public class GameLoad {
	
	long gameLoadSize;
	long gameLoadTime;
	int gameRequestCount;
	int nonGameRequestCount;
	int totalRequestCount;
	/**
	 * @return the gameLoadSize
	 */
	public long getGameLoadSize() {
		return gameLoadSize;
	}
	/**
	 * @param gameLoadSize the gameLoadSize to set
	 */
	public void setGameLoadSize(long gameLoadSize) {
		this.gameLoadSize = gameLoadSize;
	}
	/**
	 * @return the gameLoadTime
	 */
	public long getGameLoadTime() {
		return gameLoadTime;
	}
	/**
	 * @param gameLoadTime the gameLoadTime to set
	 */
	public void setGameLoadTime(long gameLoadTime) {
		this.gameLoadTime = gameLoadTime;
	}
	/**
	 * @return the gameRequestCount
	 */
	public int getGameRequestCount() {
		return gameRequestCount;
	}
	/**
	 * @param gameRequestCount the gameRequestCount to set
	 */
	public void setGameRequestCount(int gameRequestCount) {
		this.gameRequestCount = gameRequestCount;
	}
	/**
	 * @return the nonGameRequestCount
	 */
	public int getNonGameRequestCount() {
		return nonGameRequestCount;
	}
	/**
	 * @param nonGameRequestCount the nonGameRequestCount to set
	 */
	public void setNonGameRequestCount(int nonGameRequestCount) {
		this.nonGameRequestCount = nonGameRequestCount;
	}
	/**
	 * @return the totalRequestCount
	 */
	public int getTotalRequestCount() {
		return totalRequestCount;
	}
	/**
	 * @param totalRequestCount the totalRequestCount to set
	 */
	public void setTotalRequestCount(int totalRequestCount) {
		this.totalRequestCount = totalRequestCount;
	}
	
	
	

}
